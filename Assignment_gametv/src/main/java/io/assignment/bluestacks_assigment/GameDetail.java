package io.assignment.bluestacks_assigment;


public class GameDetail {
	
	int serialNumber;
	String gameName;
	String pageUrl;
	int pageStatusCode;
	String tournamentCount;
	
	public int getSerialNumber() {
		return serialNumber;
	}
	public void setSerialNumber(int serialNumber) {
		this.serialNumber = serialNumber;
	}
	public String getGameName() {
		return gameName;
	}
	public void setGameName(String gameName) {
		this.gameName = gameName;
	}
	public String getPageUrl() {
		return pageUrl;
	}
	public void setPageUrl(String pageUrl) {
		this.pageUrl = pageUrl;
	}
	public int getPageStatusCode() {
		return pageStatusCode;
	}
	public void setPageStatusCode(int pageStatusCode) {
		this.pageStatusCode = pageStatusCode;
	}
	public String getTournamentCount() {
		return tournamentCount;
	}
	public void setTournamentCount(String tournamentCount) {
		this.tournamentCount = tournamentCount;
	}

}
